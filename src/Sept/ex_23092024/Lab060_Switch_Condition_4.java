package Sept.ex_23092024;

public class Lab060_Switch_Condition_4 {
    public static void main(String[] args) {
        // No break after first case hence both the output should display
        char code = 'c';
        switch (code){
            default:
                System.out.println("Helloooo");
            case 'A':
                System.out.println("65");
                break;
            case 'B':
                System.out.println("66");
                break;
    }
}}
