package Sept.ex_25092024;

public class Lab077_For_VS_While {
    public static void main(String[] args) {
        //While - A,B,C
        //A - Initiation
        //B - Condition
        //C - Increment/Decrement

        int i=0; //A
        while (i<=10){ //B
            System.out.println(i);
            i++; //C

            for (int j = 0; j < 10; j++) {
                System.out.println(i);
                
            }

        }
    }
}
