package Sept.ex_30092024;

public class Lab092 {
    public static void main(String[] args) {
        //Calculate the Sum of Numbers from 1 to 100 with While Loop
        int sum = 0;
        int i = 1;
        while (i<=100){
            sum = sum + i;
            i++;
        }
        System.out.println(sum);
    }
}
