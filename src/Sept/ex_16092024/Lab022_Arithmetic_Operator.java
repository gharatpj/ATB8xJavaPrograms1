package Sept.ex_16092024;

public class Lab022_Arithmetic_Operator {
    public static void main(String[] args) {
        //Arithmetic Operator
        //+,-,/,*

        int a = 10;
        int b = 2;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a/b);
        System.out.println(a*b);

        //% - Modulus - Reminders
        System.out.println(a%b);
    }
}
